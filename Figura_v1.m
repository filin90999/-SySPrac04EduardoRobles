f = @(t) exp(-t);
t=linspace(-2,2,400);
hold on;
plot(t,f(t),'LineWidth',3);
xlabel('t');
ylabel('f(t)=exp(-t)');
xlim([-2 2]);
ylim([-0.5 4]);
P1=[-1 0];
P2=[-1 f(-1)];
plot([P1(1) P2(1)],[P1(2) P2(2)],'LineWidth',3,'Color',[1 0 0]);
P3=[1 0];
P4=[1 f(1)];
plot([P3(1) P4(1)],[P3(2) P4(2)],'LineWidth',3,'Color',[0 1 0]);
P3=[-1 0];
P4=[1 0];
plot([P3(1) P4(1)],[P3(2) P4(2)],'LineWidth',3,'Color',[0 1 1]);
grid on;
t=linspace(-1,1,400);
area(t,f(t));
legend('f(t)','r1','r2','r3','área');